''' calcule e mostre: o produto do dobro do primeiro com metade do segundo'''
n1= float(input("Digite um numero: "))
n2= float(input("Digite outro numero: "))
n3= float(input("Digite outro numero: "))

produto= (n1*2)*(n2/2)
soma= 3*n1+n3
elevado= n3**3

print("\nO produto do dobro do primeiro com metade do segundo é: {0:.1f}".format(produto))
print("A soma do triplo do primeiro com o terceiro é: {0:.1f}".format(soma))
print("O terceiro elevado ao cubo é: {0:.1f}".format(elevado))